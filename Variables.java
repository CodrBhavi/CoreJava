package CoreJava;

public class Variables {
    public static void main(String[] args) {
        int num = 100;
        float num_2 = 500f;
        boolean num_3 = true;
        String num_4 = "Java is fun!";
        System.out.println(num);
        System.out.println(num_2);
        System.out.println(num_3);
        System.out.println(num_4);
    }
}